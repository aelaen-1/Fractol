#include "ft_printf.h"

int	ft_putunbr(unsigned int n)
{
	unsigned int	nb;
	unsigned int	i;

	nb = n;
	i = 1;
	if (nb > 9)
	{
		ft_putnbr(nb / 10);
		ft_putnbr(nb % 10);
	}
	else
		ft_putchar(nb + 48);
	while (nb > 9)
	{
		nb = nb / 10;
		i++;
	}
	return (i);
}
